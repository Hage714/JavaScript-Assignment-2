//An ass. operator assigns a value to its left operand based on the value of its right operand
var a = 10
var b = 4

//Shorthand addition operator
a += b   //a=a+b
console.log(a)

//Shorthand subtraction operator
a -= b  //a = a - b
console.log(a)

//Shorthand multiplication operator
a *= b  //a = a * b
console.log(a)

//Shorthand division operator
a /= b  //a = a / b
console.log(a)

