/*
    1. Subtraction, the sign is - 
    2. Addition, the sign is +
    3. Division, the sign is /
    4. Multiplication, the sign is *
    5. Modulus, the sign is %
*/

var first_num = 100;
var second_num = 3;

//Adding
var sum = first_num + second_num
console.log(sum)

//Subtraction
var difference = first_num - second_num
console.log(difference)

//Multiplication
var product = first_num * second_num
console.log(product)

//Division
var division = first_num / second_num
console.log(division)

//Modulus
var modulus = first_num % second_num
console.log(modulus)

var result = 33.333333333333336
var roundoff = Math.round(result)
console.log(roundoff)

//Math. ceil() function returns the smallest integer greater than or equal to a given number
var ceilings = Math.ceil(result)
console.log(ceilings)

//floor() method returns the largest integer less than or equal to a given number
var floors = Math.floor(result)
console.log(floors)