var a = 10
var b = 3

//Greater than
console.log(a > b)  //returns true

//Less than
console.log(a < b)  //returns false

//Greater than or equal to
console.log(a >= b)  //returns true

//Less than or equal to
console.log(a <= b)  //returns false

//Equality check
console.log(a == b)  //returns false

//Strict equality check
console.log(a === b)  //returns false

//Not equal
console.log(a!= b)  //returns true

//Strict not equal
console.log(a!== b)  //returns true

